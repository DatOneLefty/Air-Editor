function hex2a(hex) { // from https://jsfiddle.net/Guffa/uT2q5/
    var str = '';
    for (var i = 0; i < hex.length; i += 2) str += String.fromCharCode(parseInt(hex.substr(i, 2), 16));
    return str;
}
